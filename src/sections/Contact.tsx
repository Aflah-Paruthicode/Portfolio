import { Button } from "@/components/Button";
import { AlertCircle, CheckCircle, Linkedin, Mail, MapPin, Send } from "lucide-react";
import type React from "react";
import { useState } from "react";
import emailjs from "@emailjs/browser";

const contact = [
  {
    icon: Mail,
    label: "Email",
    value: "amaflu4424@gmail.com",
    href: "mailto:amaflu4424@gmail.com",
  },
  {
    icon: Linkedin,
    label: "Linkedin",
    value: "Muhammed Aflah",
    href: "https://www.linkedin.com/in/muhammed-aflah-24b58725b/",
  },
  {
    icon: MapPin,
    label: "Location",
    value: "Malappuram, Kerala",
    href: "#",
  },
];

interface FormDataInterface {
  name: string;
  email: string;
  message: string;
}

export const Contact = () => {
  const [formData, setFormData] = useState<FormDataInterface>({
    name: "",
    email: "",
    message: "",
  });

  interface SubmitStatusInterface {
    type: null | string;
    message: string;
  }

  const [isLoading, setIsLoading] = useState<boolean>(false);
  const [formSubmitStatus, setFormSubmitStatus] = useState<SubmitStatusInterface>({
    type: null,
    message: "",
  });

  const handleFormSubmit = async (e: React.FormEvent<HTMLFormElement>) => {
    e.preventDefault();

    setIsLoading(true);
    setFormSubmitStatus({ type: null, message: "" });

    try {
      const serviceId = import.meta.env.VITE_EMAILJS_SERVICE_ID as string;
      const templateId = import.meta.env.VITE_EMAILJS_TEMPLATE_ID as string;
      const publicKey = import.meta.env.VITE_EMAILJS_PUBLIC_KEY as string;

      if (!serviceId || !templateId || !publicKey) throw new Error("EmailJS configuration issue");

      await emailjs.send(
        serviceId,
        templateId,
        {
          name: formData.name,
          email: formData.email,
          message: formData.message,
        },
        publicKey
      );

      setFormSubmitStatus({
        type: "success",
        message: `Message sent successfully. I'll back to you as soon as possible!`,
      });
    } catch (err) {
      console.error("Email js Error - ", err);
      setFormSubmitStatus({
        type: "error",
        message: "Failed to send message. Please try again later.",
      });
    } finally {
      setIsLoading(false);
    }
  };

  return (
    <section id="contact" className="py-32 relative overflow-hidden">
      <div className="absolute top-0 left-0 w-full h-full">
        <div className="absolute top-1/4 left-1/4 w-96 h-96 bg-primary/5 rounded-full blur-xl" />
        <div className="absolute bottom-1/4 right-1/4 w-64 h-64 bg-highlight/5 rounded-full blur-xl" />
      </div>

      <div className="container mx-auto px-6 relative z-10">
        <div className="text-center max-w-3xl mx-auto mb-16">
          <span className="text-secondary-foreground  text-sm font-medium tracking-wider uppercase animate-fadeIn">Get In Touch</span>

          <h2 className="text-4xl md:text-5xl font-bold mt-4 mb-6 animate-fadeIn animatoin-delay-100 text-secondary-foreground">
            Let's build
            <span className="font-serif italic font-normal text-white"> something great.</span>
          </h2>

          <p>Have a project in mind? I'd love to hear about it. Send me a message and let's discuss how we can work together.</p>
        </div>

        <div className="grid lg:grid-cols-2 gap-12 max-w-5xl mx-auto">
          <div className="glass p-8 rounded-3xl border border-primary/30 animate-fadeIn animation-delay-300">
            <form action="" className="space-y-6" onSubmit={handleFormSubmit}>
              <div>
                <label htmlFor="name" className="block text-sm font-medium mb-2">
                  Name
                </label>
                <input
                  id="name"
                  type="text"
                  required
                  value={formData.name}
                  onChange={(e) => setFormData({ ...formData, name: e.target.value })}
                  placeholder="Your name..."
                  className="w-full px-4 py-3 bg-surface rounded-xl border border-border focus:border-primary focus:ring-1 focus:ring-primary outline-none"
                />
              </div>

              <div>
                <label htmlFor="email" className="block text-sm font-medium mb-2">
                  Email
                </label>
                <input
                  type="email"
                  required
                  value={formData.email}
                  onChange={(e) => setFormData({ ...formData, email: e.target.value })}
                  placeholder="your@email.com"
                  className="w-full px-4 py-3 bg-surface rounded-xl border border-border focus:border-primary focus:ring-1 focus:ring-primary outline-none"
                />
              </div>

              <div>
                <label htmlFor="message" className="block text-sm font-medium mb-2">
                  Message
                </label>
                <textarea
                  rows={5}
                  required
                  value={formData.message}
                  onChange={(e) => setFormData({ ...formData, message: e.target.value })}
                  placeholder="Your message..."
                  className="w-full px-4 py-3 bg-surface rounded-xl border border-border focus:border-primary focus:ring-1 focus:ring-primary outline-none resize-none"
                />
              </div>

              <Button className="w-full" size="lg" type="submit" disabled={isLoading}>
                <>
                  {isLoading ? (
                    <>
                      Sending...
                    </>
                  ) : (
                    <>
                      Send Message
                      <Send className="h-5 w-5" />
                    </>
                  )}
                </>
              </Button>
              {formSubmitStatus.type && (
                <div
                  className={`flex items-center gap-3
                     p-4 rounded-xl ${
                       formSubmitStatus.type === "success"
                         ? "bg-green-500/10 border border-green-500/20 text-green-400"
                         : "bg-red-500/10 border border-red-500/20 text-red-400"
                     }`}
                >
                  {formSubmitStatus.type === "success" ? (
                    <CheckCircle className="w-5 h-5 shrink-0" />
                  ) : (
                    <AlertCircle className="w-5 h-5 shrink-0" />
                  )}
                  <p className="text-sm">{formSubmitStatus.message}</p>
                </div>
              )}
            </form>
          </div>

          <div className="space-y-6 animate-fadeIn animation-delay-400">
            <div className="glass rounded-3xl p-8">
              <h3 className="text-xl font-semibold mb-6">Contact Information</h3>
              <div className="space-y-4">
                {
                  contact.map((item,ind) => (
                    <a key={ind} href={item.href} className="flex items-center gap-4 p-4 rounded-xl hover:bg-surface transition-colors group" >
                      <div className="w-12 h-12 rounded-xl bg-primary/10 flex items-center justify-center group-hover:bg-primary/20 transition-colors">
                      <item.icon className="w-5 h-5 text-primary" />
                      </div>
                      <div>
                        <div className="text-sm text-muted-foreground"> {item.label}</div>
                        <div className="font-medium">{item.value}</div>
                      </div>
                    </a>
                  ))
                }
              </div>
            </div>

            <div className="glass rounded-3xl p-8 border border-primary/30">
              <div className="flex items-center gap-3 mb-4">
                <span className="w-3 h-3 bg-green-500 rounded-full animate-pulse" />
                <span className="font-medium">Currently Available</span>
              </div>

              <p>
                I'm currently open to new oppertunies and exciting projects.
                Wether you need a full-time engineer or a freelance consultant,
                let's talk!
              </p>
            </div>
          </div>
        </div>
      </div>
    </section>
  );
};
